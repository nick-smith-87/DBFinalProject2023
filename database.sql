    Win INT,
    Loss INT,
    Tie INT,
    madePlayoffs INT,
    Salary_of_Players_Used INT,
    Conference VARCHAR(3),
    Division VARCHAR(6)
);

CREATE TABLE plays_for (
    PlayerID VARCHAR(4),
    TeamID VARCHAR(3)
